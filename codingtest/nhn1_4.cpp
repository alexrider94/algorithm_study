void solution(vector<long long> &stationDis, vector<long long> &stationOil, long long &distance, long long &leftOil)
{
    long long curOil = leftOil;
    long long sum = 0;
    long long count = 0;
    long long curDistance = 0;
    int len = stationDis.size();
    for (int i = 0; i < stationOil.size(); ++i)
    {
        sum += stationOil[i];
    }
    //거리 > 총 오일
    if (distance > sum + curOil)
    {
        cout << -1;
        return;
    }
    //최소 횟수 구하기
    int i = 0;
    while (curDistance != distance)
    {
        curOil--;
        curDistance++;
        if (curDistance == stationDis[i]) //해당 스테이션 도착
        {
            if (curOil == 0 || curOil < stationDis[i + 1] - stationDis[i] && i < len - 1) //오일이 없거나 다음역까지의 거리가 오일양보다 클때 오일채우기
            {
                curOil += stationOil[i];
                count++;
            }
            else if (i == len - 1 && curOil < distance - stationDis[i]) // 마지막역에서 끝까지 못갈경우
            {
                curOil += stationOil[i];
                count++;
            }
            i++;
        }
    }
    cout << count;
}

int main()
{
    long long N, G, M;  // N = 운전해야할 거리, G = 버스에 있는 기름 양, M = 주유소의 개수
    cin >> N >> G >> M; //첫번쨰줄
    vector<long long> distance(M);
    vector<long long> mount(M);
    for (long long i = 0; i < M; ++i)
    {
        cin >> distance[i]; //두번쨰 줄 주유소 위치
    }

    for (long long i = 0; i < M; ++i)
    { //세번쨰 줄 주유소 기름 양
        cin >> mount[i];
    }
    solution(distance, mount, N, G);

    return 0;
}